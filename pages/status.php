  <section class="pb-5 actu4">
    <div class="container py-4">
      <div class="divh2co text-center"><h2><strong>Status et réglements</strong></h2></div>
      <hr class="hr"><br>
      <div class="divh5co"><h5><strong>Article 1 : Constitution et dénomination</strong></h5></div>
      <p>Il est fondé entre les adhérents aux présents statuts une Association régie par la loi du 1er 
      juillet 1901 et le décret du 16 août 1901 ayant pour titre : « Alumni Numeric School ».
      </p>
      <div class="divh5co"><h5><strong>Article 2 : Objet</strong></h5></div>
      <div class="divh6co"><h6><strong>L'Association a pour objet de :</strong></h6></div>
      <p>créer et entretenir entre les anciens élèves de Metz Numeric School des relations amicales ;
      venir en aide aux sociétaires et leur apporter différents services concrets et pratiques en 
      particulier dans le domaine du placement ;
      contribuer au développement de l'influence de la gestion française dans le public à l'étranger ;
      assurer la promotion des diplômés de Metz Numeric School ;
      favoriser la création et la reprise d'entreprises, le développement des échanges tant en 
      France qu'à l'international.
      </p>
      <div class="divh5co"><h5><strong>Article 3 : Siège Social</strong></h5></div>
      <p>Le siège social est fixé à Metz ; la dénomination de l'adresse exacte est de la compétence du 
      Conseil d'Administration.
      </p>
      <div class="divh5co"><h5><strong>Article 4 : Durée</strong></h5></div>
      <p>La durée de l'association est illimitée.</p>
      <div class="divh5co"><h5><strong>Article 5 : Composition</strong></h5></div>
      <div class="divh6co"><h6><strong>L'Association se compose :</strong></h6></div>
      <p>de membres d'honneur : sont membres d'honneur ceux qui ont rendu des services signalés 
      à l'Association. Ils sont dispensés de cotisation mais conservent le droit de participer avec 
      voix délibérative aux Assemblées Générales. Ils sont choisis chaque année à l'unanimité des 
      membres présents par le Conseil d'Administration ou reconduits tacitement ;
      de membres bienfaiteurs : ce sont des personnes qui versent une cotisation exceptionnelle ;
      de membres actifs : sont appelés membres actifs les membres de l'Association qui 
      participent régulièrement aux activités. Ils paient une cotisation annuelle.
      </p>
      <div class="divh5co"><h5><strong>Article 6 : Cotisation</strong></h5></div>
      <p>La cotisation due par chaque catégorie de membres, sauf pour les membres d'honneur, est 
      fixée annuellement par l'Assemblée Générale Ordinaire. La cotisation est due au 1er janvier de 
      l'année. Dès la première année de leur admission à Metz Numeric School, les étudiants peuvent 
      librement adhérer à l'association, moyennant une cotisation annuelle spécifique.
      </p>
      <p>Les anciens élèves qui affrontent de graves difficultés matérielles, morales ou physiques, 
      peuvent être dispensés à titre temporaire ou définitif du versement de la cotisation annuelle, 
      sur décision au cas par cas du conseil d'administration.
      </p>
      <div class="divh5co"><h5><strong>Article 7 : Conditions d'adhésion</strong></h5></div>
      <p>Pour faire partie de l'Association, il faut être élève ou ancien élève de Metz Numeric School 
      et être agréé par le bureau qui statue. En cas de refus, celui-ci n'a pas à faire connaître le motif 
      de la décision.
      Chaque membre prend l'engagement de respecter les présents statuts et le règlement intérieur 
      qui lui sont communiqués à son entrée dans l'Association.
      La qualité de membre est acquise ou maintenue après le paiement de la cotisation annuelle.
      </p>
      <div class="divh5co"><h5><strong>Article 8 : Perte de la qualité de membre</strong></h5></div>
      <div class="divh6co"><h6><strong>La qualité de membre se perd par :</strong></h6></div>
      <p>décès ;
      démission adressée par écrit au Président de l'Association ;
      exclusion prononcée par le Conseil d'Administration pour infraction aux présents statuts ou 
      motif grave portant préjudice moral ou matériel à l'Association, l'intéressé ayant été invité 
      par lettre recommandée à se présenter devant le Conseil d'Administration pour fournir des 
      explications.
      </p>
      <div class="divh5co"><h5><strong>Article 9 : Responsabilité des membres</strong></h5></div>
      <p>Aucun membre de l'Association n'est personnellement responsable des engagements contractés 
      par elle. Seul le patrimoine de l'Association répond de ses engagements.
      </p>
      <div class="divh5co"><h5><strong>Article 10 : Le Conseil d'Administration</strong></h5></div>
      <p>L'Association est administrée par un Conseil d'Administration comprenant au moins 3 et au plus 
      24 membres élus pour 3 ans par l'Assemblée Générale et choisi en son sein. Ils sont élus au scrutin 
      secret. Cependant, si personne ne s'y oppose au moment du scrutin, l'élection peut avoir lieu à 
      main levée.
      </p>
      <p>Le Conseil d'Administration, une fois ses membres élus, se réunit pour élire en son sein un Président, 
      pour une durée d'un an renouvelable.
      </p>
      <p>Est éligible tout membre jouissant de ses droits civils et politiques. Les membres sortants sont 
      rééligibles. En cas de vacance, le Conseil d'Administration pourvoit provisoirement au remplacement 
      de ses membres. Il est procédé à leur remplacement définitif par la plus prochaine Assemblée Générale.
      </p>
      <p>Ces candidatures doivent être adressées au siège de Metz Numeric School 15 jours au moins avant 
      l'Assemblée Générale, par lettre simple ou recommandée ou remise par les intéressés eux-mêmes 
      au Secrétaire Général.
      </p>
      <div class="divh5co"><h5><strong>Article 11 : Réunion du Conseil</strong></h5></div>
      <p>Le Conseil d'Administration se réunit au moins une fois par trimestre et chaque fois qu'il est 
      convoqué par écrit par son Président ou sur la demande du quart de ses membres.
      </p>
      <p>Un quorum du 1/3 de ses membres est nécessaire à la validité des délibérations et les séances 
      font l'objet de procès verbaux transcrits sans blanc, ni rature sur un registre tenu à cet effet et 
      signés par le Président et le Secrétaire Général ou le Secrétaire de séance.
      </p>
      <p>Les décisions sont prises à la majorité des voix des présents : en cas de partage, la voix du Président 
      est prépondérante.
      </p>
      <p>Tout membre du Conseil d'Administration qui aura, sans excuse reconnue valable, manqué 3 séances 
      consécutives du Conseil d'Administration, pourra après avertissement être considéré comme 
      démissionnaire et sera remplacé conformément à l'article 10.
      </p>
      <div class="divh5co"><h5><strong>Article 12 : Rémunération</strong></h5></div>
      <p>Les membres du Conseil d'Administration ne peuvent avoir aucune rétribution à raison des 
      fonctions qui leur sont confiées. Toutefois les frais et débours occasionnés par l'accomplissement 
      de leur mandant leur sont remboursés au vu des pièces justificatives. Le rapport financier 
      présenté à l'Assemblée Générale Ordinaire doit faire mention des remboursements de frais de 
      mission, de déplacement ou de représentation payés à des membres du Conseil d'Administration.
      </p>
      <div class="divh5co"><h5><strong>Article 13 : Pouvoir du Conseil</strong></h5></div>
      <p>Le Conseil d'Administration  est investi des pouvoirs les plus étendus dans la limite des buts de 
      l'Association pour faire autoriser tous actes et opérations qui ne sont pas du ressort des 
      Assemblées Générales.
      </p>
      <p>Il surveille notamment la gestion des membres du bureau et a toujours le droit de se faire rendre 
      compte de leurs actes. Il peut en cas de faute grave, suspendre les membres du bureau à la majorité.
      </p>
      <p>Il autorise le Président et le Trésorier à ouvrir tous comptes en banques, aux chèques postaux 
      et auprès de tous autres établissements de crédit, à effectuer tous emplois de fonds, solliciter 
      toutes subventions, requérir toutes inscriptions et transcriptions utiles.
      </p>
      <div class="divh5co"><h5><strong>Article 14 : Le Bureau de Direction</strong></h5></div>
      <div class="divh6co"><h6><strong>Le Conseil d'Administration élit chaque année, à l'issue de l'Assemblée Générale Ordinaire 
      Annuelle, au scrutin secret, un bureau comprenant :
      </strong></h6></div>
      <p>-         un Président;</p>
      <p>-         deux Vice présidents;</p>
      <p>-         un Délégué Général;</p>
      <p>-         un Trésorier;</p>
      <p>-         et autant de Secrétaires qu'il est nécessaire.</p>
      <p>Lors de cette séance, c'est le membre du Conseil d'Administration le plus âgé qui préside. 
      Les membres sortants sont rééligibles. Le Bureau se réunit au moins une fois par mois.
      </p>
      <div class="divh5co"><h5><strong>Article 15 : Rôle des membres du Bureau</strong></h5></div>
      <p>-         Le Président dirige les travaux du Conseil d'Administration et assure la fonction de 
          l'Association qu'il représente en justice et dans tous les actes de la vie civile. 
          Il ordonnance les dépenses. En cas d'empêchement, le Président peut déléguer ses pouvoirs 
          à un autre membre du Bureau, notamment à l'un des deux Vice présidents ;
      </p>
      <p>-         Sur délégation du Président ou de la majorité du bureau, et sous leur autorité, les vices 
          présidents peuvent être chargés de représenter l'association dans ses relations avec le monde 
          éducatif et les organisations professionnelles tant en France qu'à l'étranger.
      </p>
      <p>-         Le Délégué Général est chargé de tout ce qui concerne la correspondance, notamment l'envoi 
          des diverses convocations. Il rédige les procès-verbaux des séances tant du Conseil 
          d'Administration que des Assemblées Générales et en assure la transcription sur les registre 
          prévus à cet effet. C'est lui qui tient le registre spécial prévu par la loi du 1er juillet 1901 ;
      </p>
      <p>-         Le Trésorier tient les comptes de l'association. Il effectue tous paiement et perçoit toutes 
          recettes sous la surveillance du Président. Il tient une comptabilité régulière des recettes 
          et des dépenses faisant apparaître annuellement un compte d'exploitation, le résultat de 
          l'exercice et le bilan, et rend compte à l'Assemblée Générale qui statue sur la gestion ;
      </p>
      <p>-         Les Secrétaires se voient confier par le Président l'animation de sections particulières 
          concernant la réalisation de l'objet de l'Association. Les secrétaires ne peuvent en aucun 
          cas engager l'Association tant sur les plans juridiques et financier sans l'accord écrit et 
          préalable du Président.
      </p>
      <div class="divh5co"><h5><strong>Article 16 : Dispositions communes pour la tenue des Assemblées Générales</strong></h5></div>
      <p>Les Assemblées Générales se composent de tous les membres de l'Association à jour de leurs 
      cotisations  (au titre de l'année civile précédente). Les Assemblées se réunissant sur convocation 
      du Président ou sur la demande d'au moins ¼ des membres de l'Association.
      </p>
      <p>Les convocations doivent mentionner obligatoirement l'ordre du jour prévu et fixé par les soins 
      du Conseil d'Administration. Elles sont faites par lettres adressées aux membres  trente jours au 
      moins à l'avance.
      </p>
      <p>Seules seront valables les résolutions prises par l'Assemblée Générale sur les points inscrits à 
      son ordre du jour.
      </p>
      <p>La présidence de l'Assemblée Générale appartient de droit au Président, celui-ci peut déléguer 
      ses fonctions à un autre membre du Conseil d'Administration en cas d'empêchement.
      </p>
      <p>Le Bureau de l'Assemblée est celui de l'Association.</p>
      <p>Les délibérations sont constatées par des procès-verbaux inscrits sur un registre spécial et signé 
      par le Président et le Secrétaire Général.
      </p>
      <p>Seuls auront droit de vote les membres présents ou représentés.</p>
      <p>Il est également tenu une feuille de présence qui est signée par chaque membre présent et certifiée 
      conforme par le Bureau de l'Assemblée.
      </p>
      <p>Les Assemblées Générales régulièrement constituées représentent l'universalité des membres 
      de l'Association. Dans la limite des pouvoirs qui leur sont confiés par les présents statuts, 
      les Assemblées obligent par leurs décisions, tous les membres y compris les absents.</p>
      <div class="divh5co"><h5><strong>Article 17 : Assemblée Générale Ordinaire</strong></h5></div>
      <p>Au moins une fois par an, les adhérents sont convoqués en Assemblée Générale Ordinaire dans 
      les conditions prévues à l'article 16.
      </p>
      <p>L'Assemblée entend les rapports sur la gestion du Conseil d'Administration notamment sur la 
      situation morale et financière de l'Association.
      </p>
      <p>Le commissaire aux comptes donne lecture de son rapport de vérification.</p>
      <p>L'Assemblée, après avoir délibéré et statué sur ces différents rapports, approuve les comptes 
      de l'exercice clos ; vote le budget prévisionnel de l'exercice suivant et délibère sur toutes les 
      autres questions figurant à l'ordre du jour.
      </p>
      <p>Elle pourvoit au renouvellement des membres du Conseil d'Administration sortants dans les 
      conditions prévues à l'article 10.
      </p>
      <p>L'Assemblée Générale Ordinaire désigne également pour un an, le ou les commissaires aux 
      comptes qui sont chargés de la vérification annuelle de la gestion du Trésorier.
      </p>
      <p>Elle fixe aussi le montant de la cotisation annuelle à verser par les différentes catégories de 
      membres de l'Association.
      </p>
      <p>Les décisions de l'Assemblée Générale Ordinaire sont prises à la majorité des membres présents 
      ou représentés.
      </p>
      <p>Les délibérations sont prises à main levée sauf pour le renouvellement des membres du Conseil 
      d'Administration où il est procédé conformément  à l'article 10.
      </p>
      <div class="divh5co"><h5><strong>Article 18 : Assemblée Générale Extraordinaire</strong></h5></div>
      <p>Elle est convoquée si besoin est, dans les conditions prévues à l'article 16.</p>
      <p>L'Assemblée Générale Extraordinaire statue sur les questions qui sont de sa seule compétence, 
      à savoir les modifications à apporter aux présents statuts et dissolution.
      </p>
      <p>Les délibérations sont prises obligatoirement à la majorité des 2/3 des membres présents 
      ou représentés.
      </p>
      <p>Les votes ont lieu à main levée sauf si le ¼ au moins des membres présents ou représentés 
      exige le vote secret.
      </p>
      <div class="divh5co"><h5><strong>Article 19 : Ressources de l'Association</strong></h5></div>
      <div class="divh6co"><h6><strong>Les ressources de l'Association se composent :</strong></h6></div>
      <p>-         des produits des cotisations versées par les membres;</p>
      <p>-         des subventions éventuelles de l'Etat, des collectivités locales et territoriales, 
          des établissements publics;
      </p>
      <p>-         du produit des fêtes et manifestations, intérêts et redevances des biens et valeurs qu'elle 
          pourrait posséder ainsi que des rétributions pour services rendus;
      </p>
      <p>-         de toutes autres ressources ou subventions qui ne seraient pas contraires aux lois en vigueur.
      </p>
      <div class="divh5co"><h5><strong>Article 20 : Règlement intérieur</strong></h5></div>
      <p>L'établissement du règlement intérieur est de la compétence du Conseil d'Administration.
      </p>
      <p>Il fixe les divers points non prévus par les statuts notamment ceux qui ont trait au 
      fonctionnement pratique de l'Association.
      </p>
      <div class="divh5co"><h5><strong>Article 21 : Contrôle</strong></h5></div>
      <p>Il est nommé un ou deux contrôleurs des comptes qui ont pour missions de s'assurer de la 
      régularité de la comptabilité et d'en rendre compte à l'Assemblée Générale Ordinaire. 
      Ils ne peuvent remplir de fonction au sein du Conseil d'Administration.
      </p>
      <div class="divh5co"><h5><strong>Article 22 : Dissolution</strong></h5></div>
      <p>En cas de dissolution prononcée par les deux tiers au moins des membres présents à 
      l'Assemblée Générale Extraordinaire un ou deux liquidateurs sont nommés par celle-ci et 
      l'actif s'il y a lieu est dévolu conformément à l'article 9 de la loi du 1er juillet 1901 et du 
      décret du 16 août 1901.
      </p>
      <div class="divh5co"><h5><strong>Article 23 : Exercice social</strong></h5></div>
      <p>Il s'étend du 1er janvier au 31 décembre.</p>
      <div class="divh5co"><h5><strong>Article 24 : Formalités administratives</strong></h5></div>
      <p>Le Président du Conseil d'Administration doit accomplir toutes les formalités de déclaration
      et de publication prévues par la loi du 1er juillet 1901 et par décret du 16 août 1901 tant au 
      moment de la création de l'Association qu'au cours de son existence ultérieure.
      </p>
      <p>En particulier, il doit faire connaître dans les trois mois à la préfecture tous les changements 
      survenus dans l'administration ou la Direction et de présenter sans déplacement les registres 
      et pièces de comptabilité sur toute réquisition du Préfet, à lui-même ou à son délégué.</p>
      <hr class="hr"><br><br><br>
    </div>
  </section>
